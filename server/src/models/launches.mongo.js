import mongoose from "mongoose";
const launchesSchema = new mongoose.Schema({
  flightNumber: {
    required: true,
    type: Number,
  },
  launchDate: {
    type: Date,
    required: true,
  },
  mission: {
    type: String,
    required: true,
  },
  rocket: {
    type: String,
    required: true,
  },
  target: {
    type: String,
    required: true,
  },
  customer: [String],
  upcoming: {
    type: Boolean,
    required: true,
  },
  success: {
    type: Boolean,
    required: true,
    default: true,
  },
});
export default mongoose.model("Launch", launchesSchema);
